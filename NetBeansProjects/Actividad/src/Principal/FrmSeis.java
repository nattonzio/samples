    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        String nom,ape,dni;
        int dia,jor;
        nom=txtnom.getText();
        ape=txtape.getText();
        dni=txtdni.getText();
        dia=Integer.parseInt(txtd.getText());
        jor=Integer.parseInt(txtj.getText());
        sc.asignarClase06(dni, nom, ape);
        td.asignarClase006(dia, jor);
        
        txts.setText(String.valueOf(td.calcularsueldo()));
        txtdsct.setText(String.valueOf(td.calculardescuento()));
        txtsn.setText(String.valueOf(td.sn()));
        
        JOptionPane.showMessageDialog(this,"DNI: "+dni+"\nTrabajador:"+ nom +" "+ape+"\n Sueldo Neto:"+String.valueOf(td.sn()),"RESULTADO", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

