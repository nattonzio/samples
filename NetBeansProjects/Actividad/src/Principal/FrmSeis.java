    private void txtdniKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdniKeyReleased
        // TODO add your handling code here:
        int s;
        char x;
        x=evt.getKeyChar();
        s=evt.getKeyCode();
        if (x<'0' || x>'9') {
            if (s!=8 && s!=16 && s!=37) {
            txtdni.setText(null);
        }
        }
        int c;
        c=txtdni.getText().length();
        if (c==8) {
            txtnom.requestFocus();
        }
    }//GEN-LAST:event_txtdniKeyReleased

