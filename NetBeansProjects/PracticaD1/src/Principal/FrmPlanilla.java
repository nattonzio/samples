    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Validando el RadioButton
        int d=0,suel=0;
        d=Integer.parseInt(txtdias.getText());
        suel=d*afi;
        txtjornal.setText(String.valueOf(afi));
        txts.setText(String.valueOf(suel));
        
        //Validando el CheckButton
        double p,a,j,sn,td;
        if (chkpres.isSelected()) {
            p=suel*0.15;
        }else{
            p=0;
        }
        if (chkalim.isSelected()) {
            a=suel*0.1;
        }else{
            a=0;
        }
        if (chkjud.isSelected()) {
            j=suel*0.05;
        }else{
            j=0;
        }
        td=p+a+j;
        sn=suel-td;
        txtpres.setText(String.valueOf(p));
        txtalim.setText(String.valueOf(a));
        txtjudi.setText(String.valueOf(j));
        txtdesc.setText(String.valueOf(td));
        txtsueldon.setText(String.valueOf(sn));
    }//GEN-LAST:event_jButton2ActionPerformed

