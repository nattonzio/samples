    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String nt1,nt2,nt3;
        nt1=txtn1.getText();
        nt2=txtn2.getText();
        nt3=txtn3.getText();
        if (nt1.equals("") ||nt2.equals("")||nt3.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Ingresar Nota", "notas", JOptionPane.INFORMATION_MESSAGE);
        }else{
        int n1,n2,n3,prom;
        n1=Integer.parseInt(txtn1.getText());
        n2=Integer.parseInt(txtn2.getText());
        n3=Integer.parseInt(txtn3.getText()); 
        prom=(n1+n2+n3)/3;
            if (prom<10) {
                lblpro.setText("JALADO");
            }else if(prom>=10 && prom<=12){
               lblpro.setText("RECUPERACION");
            }else if(prom>=13 && prom<=17){
                lblpro.setText("BUENO");
            }else{
                lblpro.setText("EXCELENTE");
            }
        txtprom.setText(String.valueOf(prom));
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

